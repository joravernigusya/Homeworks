# 5. Есть массив чисел. Известно, что каждое число в этом массиве имеет
# пару, кроме одного: [1, 5, 2, 9, 2, 9, 1] => 5. Напишите программу,
# которая будет выводить уникальное число.
list1 = [1, 5, 2, 9, 2, 9, 1]
print(*[i for i in list1 if list1.count(i) == 1])
