# 3. Найти произведение положительных, сумму и количество отрицательных
# из 10 введенных целых значений.

mult_pos_num = 1
count_neg_num = 0
sum_neg_num = 0
for i in range(-5, 5):
    if i > 0:
        mult_pos_num *= i
    elif i < 0:
        count_neg_num += 1
        sum_neg_num += i
print(
    f"Произведение положительных значений = {mult_pos_num}\n"
    f"Произведение отрицательных значений = {sum_neg_num}\n"
    f"Количество отрицательных значений = {count_neg_num}"
)

